<template>
  <div class="sys_con" :class="{sys_close:close}">
    <div class="sys_header">
      <h4><span class="sys_logo"><img src="../../indexApp3/src/assets/logo.png" id="logo1"></span>{{title}}<br /><span id="English">COLLABORATION APPLICATION PLATFORM</span></h4>
      <h5 class="sys_df" style="margin-top: 20px;position: absolute;z-index: 2">
        <el-radio-group v-model="languageValue" @change="languageChange">
          <el-radio  label="cn">中文</el-radio>
          <el-radio  label="en">英文</el-radio>
        </el-radio-group>
      </h5>
      <div class="sys_head">
        <el-dropdown @command="" class="personBox" trigger="click">
            <span class="el-dropdown-link">
              {{user}}<i class="el-icon-arrow-down el-icon--right"></i><br />
            <span>{{department}}</span>
            </span>
          <el-dropdown-menu slot="dropdown">
            <el-dropdown-item command="a">个人中心</el-dropdown-item>
            <el-dropdown-item command="b">退出</el-dropdown-item>
          </el-dropdown-menu>
        </el-dropdown>
        <div class="imgBox">
          <img src="./assets/1.png" alt="" width="50" height="50">
        </div>
      </div>
      <hr />
    </div>
    <div class="sys_route">
      <div class="sys_route_out">
        <transition name="router-fade" mode="out-in">
          <!--<keep-alive>-->
          <router-view v-if="routerType=='normal'"></router-view>
          <!--</keep-alive>-->
          <div v-if="routerType=='iframe'">
            <el-tabs v-model="editableTabsValue" type="card" closable @tab-remove="removeTab">
              <el-tab-pane
                v-for="(item, index) in editableTabs"
                :key="item.name"
                :label="item.title"
                :name="item.name"
              >
                <iframe   style="width:100%; height:500px" :src="item.src" v-if="item.src"></iframe>
                {{item.content}}
              </el-tab-pane>
            </el-tabs>
          </div>
        </transition>
      </div>
    </div>
  </div>
</template>
<script>
  import language  from "../../language/language";
  export default {
    name: "hh-index-tab",
    data(){
      return {
        close:false,
        currRoute: null,
        crumbs:[],
        cUrl: null,
        title:language.platformName,
        user:language.user,
        department:language.department,
        languageValue:"cn",
        // iframe tab 设置
        routerType:"normal",
        editableTabsValue: '1',
        editableTabs: [],
        tabIndex: 1
      }
    },
    methods: {
      languageChange(val) {
        localStorage.setItem('ELEMENT_LANGUAGE', val)
        window.location.reload();
      },
      addTab(targetName,src) {
        let newTabName = ++this.tabIndex + '';
        this.editableTabs.push({
          title: targetName,
          name: newTabName,
          src:src
        });
        this.editableTabsValue = newTabName;
      },
      removeTab(targetName) {
        let tabs = this.editableTabs;
        let activeName = this.editableTabs;
        if (activeName === targetName) {
          tabs.forEach((tab, index) => {
            if (tab.name === targetName) {
              let nextTab = tabs[index + 1] || tabs[index - 1];
              if (nextTab) {
                activeName = nextTab.name;
              }
            }
          });
        }

        this.editableTabsValue = activeName;
        this.editableTabs = tabs.filter(tab => tab.name !== targetName);
      }
    },
      computed: {
        closeMenuState() {
          return this.$store.getters.mentState;
        },

      },
      watch: {
        closeMenuState(value) {
          if (value)
            this.close = true;
          else
            this.close = false;
        },
        $route(data) {
          if (data.meta[0] &&data.meta[0].type && data.meta[0].type == 'iframe') {
            this.routerType = "iframe";
            this.addTab(data.meta[0].name, data.meta[0].url);
          }else {
            this.routerType = "normal";
          }
        }
      },
      mounted() {
        this.languageValue = localStorage.getItem('ELEMENT_LANGUAGE')
      }
    }
</script>
